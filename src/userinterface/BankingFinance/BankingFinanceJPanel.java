    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblInterestFreeLoanRequest.getModel();

        model.setRowCount(0);

        for (WorkRequest request : bankingOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[7];
            InterestFreeLoanWorkRequest interestFreeLoanWorkRequest = (InterestFreeLoanWorkRequest) request;
            row[0] = interestFreeLoanWorkRequest;
            row[1] = interestFreeLoanWorkRequest.getReceiver();
            row[2] = interestFreeLoanWorkRequest.getMonthlyIncome();
            row[3] = interestFreeLoanWorkRequest.IsAgriLand();
            row[4] = interestFreeLoanWorkRequest.IsCurrentIncome();
            row[5] = interestFreeLoanWorkRequest.IsLoanUsed();
            row[6] = interestFreeLoanWorkRequest.getStatus();
            model.addRow(row);
        }
    }

