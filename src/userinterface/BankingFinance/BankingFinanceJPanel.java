    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblInterestFreeLoanRequest.getSelectedRow();

        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) tblInterestFreeLoanRequest.getValueAt(selectedRow, 0);
            if (request.getStatus().equalsIgnoreCase("Completed")
                    || request.getStatus().equalsIgnoreCase("Request Processed and Loan Approved")
                    || request.getStatus().equalsIgnoreCase("Loan Not Approved")) {
                JOptionPane.showMessageDialog(null, "Request already processed.");
                return;
            } else {
                request.setReceiver(userAccount);
                request.setStatus("Pending");
                populateTable();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Choose a request to process.");
            return;
        }

    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void BtnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblInterestFreeLoanRequest.getSelectedRow();

        if (selectedRow >= 0) {
            InterestFreeLoanWorkRequest request = (InterestFreeLoanWorkRequest) tblInterestFreeLoanRequest.getValueAt(selectedRow, 0);
            if (request.getReceiver() == null) {
                request.setReceiver(userAccount);
            }
            //Above 200, Between 100 and 300, Below 100
            if ((request.IsAgriLand() || request.IsCurrentIncome() || request.IsLoanUsed())
                    && request.getMonthlyIncome().equals("Below 10000")) {
                request.setStatus("Request Processed and Loan Approved");
            } else {
                request.setStatus("Loan Not Approved");
            }

            populateTable();

        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
            return;
        }
    }//GEN-LAST:event_BtnProcessActionPerformed

