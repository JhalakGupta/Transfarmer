    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row ");
            return;
        }
        boolean isValid = true;
        MedicineSupplierWorkRequest request = (MedicineSupplierWorkRequest) requestTable.getValueAt(selectedRow, 0);
        Medicine medicineRequested = request.getMedicine();
        //String name = medicineRequested.getName();
        //int qty = medicineRequested.getQuantity();
        int qty = request.getQuantityNeeded();
        String name = request.getMedicine().getName();

        for (Medicine medicine : medicineInventoryOrganization.getMedicineDirectory().getMedicineList()) {

            if (medicine.getName().equals(name)) {
                if (qty > medicine.getQuantity()) {
                    isValid = false;
                    break;
                }
            }
        }

        if (isValid) {
            request.setReceiver(userAccount);
            request.setStatus("Completed");
            // update the quantity in the inventory
            for (Medicine medicine : medicineInventoryOrganization.getMedicineDirectory().getMedicineList()) {
                if (medicine.getName().equals(name)) {
                    int q = medicine.getQuantity();
                    q = q - qty;
                    medicine.setQuantity(q);
                    break;
                }
            }
            populateTable();
            JOptionPane.showMessageDialog(null, "Required Medicines sent !!");
        } else {
            JOptionPane.showMessageDialog(null, "Not enough quantity");
        }
    }//GEN-LAST:event_acceptBtnActionPerformed

