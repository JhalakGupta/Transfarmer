    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) requestTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : medicineInventoryOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[5];
            MedicineSupplierWorkRequest supplierWorkRequest = (MedicineSupplierWorkRequest) request;
            row[0] = supplierWorkRequest;
            row[1] = supplierWorkRequest.getReceiver();

            if (supplierWorkRequest.getMedicine() != null) {
                row[2] = supplierWorkRequest.getMedicineName();
                //row[3] = supplierWorkRequest.getMedicine().getQuantity();
                row[3] = supplierWorkRequest.getQuantityNeeded();
            } else {
                row[2] = null;
                row[3] = null;
            }
            row[4] = request.getStatus();

            model.addRow(row);
        }

    }

